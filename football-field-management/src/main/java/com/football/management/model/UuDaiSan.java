package com.football.management.model;

public class UuDaiSan {
    private int maUuDai;
    private int maSan;

    public UuDaiSan() {
    }

    public UuDaiSan(int maUuDai, int maSan) {
        this.maUuDai = maUuDai;
        this.maSan = maSan;
    }

    public int getMaUuDai() {
        return maUuDai;
    }

    public void setMaUuDai(int maUuDai) {
        this.maUuDai = maUuDai;
    }

    public int getMaSan() {
        return maSan;
    }

    public void setMaSan(int maSan) {
        this.maSan = maSan;
    }
}