CREATE TABLE hoa_don (
    ma_hoa_don              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ma_hoa_don_code         VARCHAR2(30) NOT NULL UNIQUE,
    ma_thanh_toan           NUMBER NOT NULL UNIQUE,
