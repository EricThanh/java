CREATE TABLE lich_su_trang_thai_dat_san (
    ma_lich_su              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ma_dat_san              NUMBER NOT NULL,
    trang_thai_cu           VARCHAR2(20),
    trang_thai_moi          VARCHAR2(20) NOT NULL,
    ma_tai_khoan_thay_doi   NUMBER,
