    ghi_chu                 VARCHAR2(255),
    CONSTRAINT fk_ls_trang_thai_dat_san FOREIGN KEY (ma_dat_san) REFERENCES dat_san(ma_dat_san),
    CONSTRAINT fk_ls_tai_khoan FOREIGN KEY (ma_tai_khoan_thay_doi) REFERENCES tai_khoan(ma_tai_khoan)
