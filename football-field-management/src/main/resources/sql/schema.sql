-- 12. SAN YEU THICH
-- =========================
CREATE TABLE san_yeu_thich (
    ma_khach_hang       NUMBER NOT NULL,
    ma_san              NUMBER NOT NULL,
    ngay_tao            DATE DEFAULT SYSDATE,
    PRIMARY KEY (ma_khach_hang, ma_san),
    CONSTRAINT fk_san_yt_khach_hang FOREIGN KEY (ma_khach_hang) REFERENCES khach_hang(ma_khach_hang),
    CONSTRAINT fk_san_yt_san FOREIGN KEY (ma_san) REFERENCES san_bong(ma_san)
);

-- =========================
-- 13. THANH TOAN
-- =========================
CREATE TABLE thanh_toan (
    ma_thanh_toan           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ma_dat_san              NUMBER NOT NULL UNIQUE,
    hinh_thuc_thanh_toan    VARCHAR2(20) NOT NULL,
    trang_thai_thanh_toan   VARCHAR2(20) NOT NULL,
    so_tien_thanh_toan      NUMBER(12,2) NOT NULL,
    thoi_gian_thanh_toan    DATE,
    ma_giao_dich            VARCHAR2(100),
    ma_nhan_vien_xac_nhan   NUMBER,
    ghi_chu                 VARCHAR2(255),
    CONSTRAINT fk_thanh_toan_dat_san FOREIGN KEY (ma_dat_san) REFERENCES dat_san(ma_dat_san),
    CONSTRAINT fk_thanh_toan_nhan_vien FOREIGN KEY (ma_nhan_vien_xac_nhan) REFERENCES nhan_vien(ma_nhan_vien),
    CONSTRAINT chk_hinh_thuc_thanh_toan 
        CHECK (hinh_thuc_thanh_toan IN ('TIEN_MAT', 'CHUYEN_KHOAN', 'THANH_TOAN_ONLINE', 'THANH_TOAN_TAI_SAN')),
    CONSTRAINT chk_trang_thai_thanh_toan 
        CHECK (trang_thai_thanh_toan IN ('CHO_XU_LY', 'THANH_CONG', 'THAT_BAI', 'CHO_XAC_NHAN')),
    CONSTRAINT chk_so_tien_thanh_toan 
        CHECK (so_tien_thanh_toan >= 0)
);

-- =========================
-- 14. HOA DON
