    tong_tien               NUMBER(12,2) NOT NULL,
    ghi_chu                 VARCHAR2(255),
    CONSTRAINT fk_hoa_don_thanh_toan FOREIGN KEY (ma_thanh_toan) REFERENCES thanh_toan(ma_thanh_toan),
    CONSTRAINT chk_tong_tien_hoa_don 
        CHECK (tong_tien >= 0)
);

