CREATE TABLE USERS (
                       USER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                       USERNAME VARCHAR2(50) NOT NULL UNIQUE,
                       PASSWORD VARCHAR2(100) NOT NULL,
                       FULL_NAME VARCHAR2(100) NOT NULL,
                       ROLE VARCHAR2(20) NOT NULL,
                       STATUS VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL
);

CREATE TABLE FIELDS (
                        FIELD_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        FIELD_NAME VARCHAR2(100) NOT NULL,
                        FIELD_TYPE VARCHAR2(20) NOT NULL,
                        DESCRIPTION VARCHAR2(255),
                        STATUS VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL
);

CREATE TABLE CUSTOMERS (
                           CUSTOMER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                           FULL_NAME VARCHAR2(100) NOT NULL,
                           PHONE VARCHAR2(20) NOT NULL UNIQUE,
                           NOTE VARCHAR2(255)
);

CREATE TABLE PRICE_RULES (
                             PRICE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                             FIELD_TYPE VARCHAR2(20) NOT NULL,
                             DAY_TYPE VARCHAR2(20) NOT NULL,
                             START_MINUTE NUMBER NOT NULL,
                             END_MINUTE NUMBER NOT NULL,
                             PRICE_PER_HOUR NUMBER(12,2) NOT NULL,
                             STATUS VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL
);

CREATE TABLE BOOKINGS (
                          BOOKING_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                          FIELD_ID NUMBER NOT NULL,
                          CUSTOMER_ID NUMBER NOT NULL,
                          BOOKING_DATE DATE NOT NULL,
                          START_MINUTE NUMBER NOT NULL,
                          END_MINUTE NUMBER NOT NULL,
                          STATUS VARCHAR2(20) DEFAULT 'PENDING' NOT NULL,
                          TOTAL_AMOUNT NUMBER(12,2) DEFAULT 0 NOT NULL,
                          DEPOSIT_AMOUNT NUMBER(12,2) DEFAULT 0 NOT NULL,
                          CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
                          CREATED_BY NUMBER,
                          CONSTRAINT FK_BOOKING_FIELD FOREIGN KEY (FIELD_ID) REFERENCES FIELDS(FIELD_ID),
                          CONSTRAINT FK_BOOKING_CUSTOMER FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMERS(CUSTOMER_ID),
                          CONSTRAINT FK_BOOKING_USER FOREIGN KEY (CREATED_BY) REFERENCES USERS(USER_ID)
);

CREATE TABLE PAYMENTS (
                          PAYMENT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                          BOOKING_ID NUMBER NOT NULL,
                          PAYMENT_DATE DATE DEFAULT SYSDATE NOT NULL,
                          AMOUNT NUMBER(12,2) NOT NULL,
                          PAYMENT_METHOD VARCHAR2(30),
                          NOTE VARCHAR2(255),
                          CONSTRAINT FK_PAYMENT_BOOKING FOREIGN KEY (BOOKING_ID) REFERENCES BOOKINGS(BOOKING_ID)
);

CREATE TABLE INVOICES (
                          INVOICE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                          BOOKING_ID NUMBER NOT NULL UNIQUE,
                          INVOICE_DATE DATE DEFAULT SYSDATE NOT NULL,
                          TOTAL_AMOUNT NUMBER(12,2) NOT NULL,
                          PAID_AMOUNT NUMBER(12,2) DEFAULT 0 NOT NULL,
                          REMAINING_AMOUNT NUMBER(12,2) DEFAULT 0 NOT NULL,
                          STATUS VARCHAR2(20) DEFAULT 'UNPAID' NOT NULL,
                          CONSTRAINT FK_INVOICE_BOOKING FOREIGN KEY (BOOKING_ID) REFERENCES BOOKINGS(BOOKING_ID)
);
